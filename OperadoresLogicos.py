# OperadoresLogicos (and,or, not)
# Sirven para comparar expresiones  boleanas o un valor booleano

# and
a = True
b = True
resultado = (a and b)
print('el operador and es:', resultado)
# or
resultado = (a or b)
print('el operador or es:', resultado)
# not
resultado = not a
print('el operador or es:', resultado)
