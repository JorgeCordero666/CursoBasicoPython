# Operadores de comparacion (==) (!=) (>) (<) (>=) (<=)
a = 4
b = 2
print('Los numeros son ', a, ' y ', b)
# (==)Iguales
resultado = (a == b)  # Se pueden comparar valores directamente :D
print('El resultado de la igualdad es: ', resultado)
# (!=)Distintos
resultado = (a != b)
print('El resultado de la diferencia es: ', resultado)
# (>) Mayor que
resultado = (a > b)
print('El numero', a, 'es mayor que ', b, ' es', resultado)
# (>=) Mayor que o igual
resultado = (a >= b)
print('El numero', a, 'es mayor o igual que ', b, ' es', resultado)
# (<) Menor
resultado = (a < b)
print('El numero', a, 'es menor que ', b, ' es', resultado)
# (<=) Menor o igual que
resultado = (a <= b)
print('El numero', a, 'es menor o igual que ', b, ' es', resultado)
