# Operadores Aritmaticos
operadorA = 3
operadorB = 2
# Suma
suma = operadorA + operadorB
print(f'Resultado de la suma: {suma} ')  # Otra forma de imprimir por consola

# Resta
resta = operadorA - operadorB
print(f'El resultado de la resta es: {resta}')
# multiplicacion
multiplicacion = operadorA * operadorB
print(f'El resultado de la multiplicacion es: {multiplicacion}')
# Division
division = operadorA / operadorB
print(f'El resultado de la division es: {division}')
division = operadorA // operadorB  # Si se quiere solo la parte entera
print(f'El resultado de la division solo el numero entero es: {division}')
# Sacar el modulo (residuo)
modulo = operadorA % operadorB
print(f'El residuo de la division es: {division}')
# Potenciacion
potencia = operadorA ** operadorB
print('La potencia de', operadorA, 'elevado a',
      operadorB, 'es igual a', potencia)
